    public void loadcombo() throws SQLException {
        Connection conn = getConnection();
        String query = "SELECT product_code FROM inventory order by product_code";
        Statement st = null;
        ResultSet rs = null;
        try {
            st = conn.createStatement();
            rs = st.executeQuery(query);
            while (rs.next()) {
                String product_code = rs.getString("product_code");
                jComboBox1.addItem(product_code);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        rs.close();
        st.close();
        conn.close();
    }
    
